    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollNo=rollNoField.getText();
        try{
            Connection con = ConnectionProviderCode.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollNo='"+rollNo+"'and result.rollNo='"+rollNo+"'");
            if(!rs.first()){
                JOptionPane.showMessageDialog(null,"Entered roll No does not exist");
            }
            else{
                //For filling data in respective fields
                nameField.setText(rs.getString(1));
                genderField.setText(rs.getString(3));
                fatherField.setText(rs.getString(4));
                courseField.setText(rs.getString(5));
                branchField.setText(rs.getString(6));
                mathField.setText(rs.getString(8));
                chemField.setText(rs.getString(9));
                mechField.setText(rs.getString(10));
                electricalField.setText(rs.getString(11));
                programmingField.setText(rs.getString(12));
                totalField.setText(rs.getString(13));
                
                //for freezing all the fields
                nameField.setEditable(false);
                genderField.setEditable(false);
                fatherField.setEditable(false);
                courseField.setEditable(false);
                branchField.setEditable(false);
                mathField.setEditable(false);
                chemField.setEditable(false);
                mechField.setEditable(false);
                electricalField.setEditable(false);
                programmingField.setEditable(false);
                totalField.setEditable(false);
                
                //verdict decesion
                int math=Integer.parseInt(rs.getString(8));
                int chem=Integer.parseInt(rs.getString(9));
                int mech=Integer.parseInt(rs.getString(10));
                int electrical=Integer.parseInt(rs.getString(11));
                int programming=Integer.parseInt(rs.getString(12));
                
                if(math>=30 && chem>=30 && mech>=30 && electrical>=30 && programming>=30){
                    verdictField.setText("PASS");
                }
                else{
                    verdictField.setText("FAIL");
                }
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

