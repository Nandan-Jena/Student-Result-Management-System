    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String rollNo=rollNoField.getText();
        int Math=Integer.parseInt(mathField.getText());
        int Chem=Integer.parseInt(chemField.getText());
        int Mech=Integer.parseInt(mechField.getText());
        int Electrical=Integer.parseInt(electricalField.getText());
        int Programming=Integer.parseInt(programmingField.getText());
        int Total = Math+Chem+Mech+Electrical+Programming;
        
        try{
            Connection con = ConnectionProviderCode.getCon();
            Statement st =con.createStatement();
            st.executeUpdate("insert into result values('"+ rollNo +"','"+ Math +"','"+ Chem +"','"+ Mech +"','" +Electrical +"','" +Programming +"','"+ Total +"')");
            JOptionPane.showMessageDialog(null, "Student Marks inserted");
            setVisible(false);
            InsertResult frame = new InsertResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
        
    }//GEN-LAST:event_saveButtonActionPerformed

    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        String rollNo=rollNoField.getText();
        try{
            Connection con = ConnectionProviderCode.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("Select * from student where rollNo='"+ rollNo +"'");
            resultTable.setModel(DbUtils.resultSetToTableModel(rs));
            if(!rs.first()){
                JOptionPane.showMessageDialog(null,"Entered roll No does not exist");
            }
            rollNoField.setEditable(false);       //once valid roll no is entered the roll field will be freezed 
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_searchButtonActionPerformed

