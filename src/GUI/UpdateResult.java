    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
                String rollNo=rollNoField.getText();
        try{
            Connection con = ConnectionProviderCode.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("Select * from result where rollNo='"+ rollNo +"'");
            if(!rs.first()){
                JOptionPane.showMessageDialog(null,"Entered roll No does not exist");
            }
            else{
                rollNoField.setEditable(false);       //once valid roll no is entered the roll field will be freezed
                mathField.setText(rs.getString(2));
                chemField.setText(rs.getString(3));
                mechField.setText(rs.getString(4));
                electricalField.setText(rs.getString(5));
                programmingField.setText(rs.getString(6));
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_searchButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        String rollNo=rollNoField.getText();
        int Math=Integer.parseInt(mathField.getText());
        int Chem=Integer.parseInt(chemField.getText());
        int Mech=Integer.parseInt(mechField.getText());
        int Electrical=Integer.parseInt(electricalField.getText());
        int Programming=Integer.parseInt(programmingField.getText());
        int Total = Math+Chem+Mech+Electrical+Programming;
        
        try{
            Connection con = ConnectionProviderCode.getCon();
            Statement st =con.createStatement();
            st.executeUpdate("Update result SET Math="+Math+",Chem="+Chem+",Mech="+Mech+",Electrical="+Electrical+",Programming="+Programming+",Total="+Total+" where rollNo="+rollNo+"");
            JOptionPane.showMessageDialog(null, "Student Marks updated");
            setVisible(false);
            UpdateResult frame = new UpdateResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }

    }//GEN-LAST:event_updateButtonActionPerformed

